let currentLanguage: Language = 'en';
const listeners = new Set<(lang: Language) => void>();

export const setLanguage = (lang: Language) => {
    currentLanguage = lang;
    localStorage.setItem('aegis_language', lang);
    listeners.forEach(listener => listener(lang));
};

export const getLanguage = (): Language => {
    return (localStorage.getItem('aegis_language') as Language) || currentLanguage;
};

// Auto-initialize from localStorage
if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('aegis_language');
    if (saved === 'es' || saved === 'en') {
        currentLanguage = saved;
    }
}

export const useTranslation = () => {
    const [lang, setLang] = useState<Language>(getLanguage());

    useEffect(() => {
        listeners.add(setLang);
        return () => {
            listeners.delete(setLang);
        };
    }, []);

    const t = useCallback((key: string, params?: Record<string, string>): string => {
        let text = dictionaries[lang]?.[key] || dictionaries['es']?.[key] || key;
        if (params) {
            Object.entries(params).forEach(([k, v]) => {
                text = text.replace(`{{${k}}}`, v);
            });
        }
        return text;
    }, [lang]);

    return { t, lang, setLanguage };
};
